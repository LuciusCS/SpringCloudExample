package com.example;

import java.util.Map;

public class MapArea {


    String name;
    String alpha;

    public MapArea(String name, String alpha) {
        this.name = name;
        this.alpha = alpha;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAlpha() {
        return alpha;
    }

    public void setAlpha(String alpha) {
        this.alpha = alpha;
    }
}
